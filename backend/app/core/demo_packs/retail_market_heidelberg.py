"""Public showcase demo: Lebensmittelmarkt Heidelberg / Retail Market Heidelberg.

Urban-infill single-storey food retail market with a structured car deck on a
constrained inner-city plot in the Pfaffengrund district of Heidelberg (DE).
Unlike the larger out-of-town Heilbronn store this is a tighter, smaller box
squeezed into a built-up commercial street: the logistics and site-setup cost
is higher (lane closures, crane provision, traffic management on a live road),
the facade is more demanding because the parcel sits inside a municipal design
board area (Gestaltungssatzung, a ceramic and brick-slip accent envelope), and
the PV array is smaller because the roof is partly shaded by the neighbouring
block. A self-contained showcase project that the partner-pack loader picks up
automatically; it shares the canonical 16-LV gewerk structure and the
VE.Abschnitt.Position OZ scheme with the other retail demos but is priced from
its own reconciled cost plan. All companies are fictional with descriptive
generic names; the only real entity is the public building authority.

Data layer
----------
The BOQ is assembled from two module-level tables so later passes only have to
touch the data, never the assembly logic:

* ``_VE_SECTIONS`` - the 16 priced procurement units (Vergabeeinheiten) that
  map 1:1 to LV sections (OZ scheme ``VE.subsection.position``). Their budgets
  sum to exactly 7,200,000.00 EUR net.
* ``_POSITIONS`` - the full LV grouped into thematic OZ subsections per Gewerk.
  Every section sums EXACTLY to its procurement-unit budget; the last lsum row
  in each section absorbs the rounding remainder so a careless rate edit can
  never silently drift a budget. ``_build_sections`` raises if any section
  drifts by a cent.

Money contract
--------------
Per LV section the position sum equals the procurement-unit budget to the cent,
and the 16 budgets sum to 7,200,000.00 EUR net. All money arithmetic runs
through ``Decimal`` so the emitted two-decimal floats are exact.

Geometry
--------
Quantities derive from the canonical building geometry of the smaller box:
footprint 60 x 36 m = 2,160 m2, work area 2,203 m2 (footprint x 1.02),
perimeter 192 m, 10 axes a 6.0 m -> 30 columns / 30 pocket foundations,
ground slab 432 m3 (2,160 x 0.20), envelope balance 980 + 320 + 110 + 39 m2
on roughly 192 x 6.6 m of skin, 520 PV modules a 440 Wp = 228.8 kWp, and a
compact car park of about 78 stalls.
"""

